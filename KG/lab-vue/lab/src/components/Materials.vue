    Поворот: Обертання об'єкта навколо заданої точки або центра координат. Поворот може бути виконаний навколо будь-якої вісі.

    Масштабування: Збільшення або зменшення розмірів об'єкта. Масштабування може бути однорідним (однаковим за всіма напрямками) або нелінійним.

    Зеркальне відображення: Створення відображення об'єкта вздовж визначеної площини. Наприклад, зеркальне відображення вздовж площини xy відображає об'єкт вздовж вісі z.

Ці операції можуть бути виражені матрицями, що дозволяє легко і ефективно використовувати їх у комп'ютерних програмах для реалізації афінних перетворень. Матриці афінних перетворень множаться на координати точок об'єкта для отримання нових координат після застосування перетворень.

