        Модель кольорів HSV (Hue, Saturation, Value) - це інша система представлення кольорів, яка відрізняється від RGB інтерпретацією кольору як комбінації відтінку, насиченості та значення (яскравості). Основні компоненти цієї моделі визначаються наступним чином:

