    Насиченість (Saturation): Визначає чистоту або яскравість кольору. Зменшення насиченості призводить до відтінків сірого. Значення насиченості вимірюється відсотками, де 0% відповідає відтінкам сірого, а 100% - максимально насиченим кольорам.

    Значення (Value): Визначає яскравість кольору. Значення 0 вказує на чорний колір, а 100 - на повністю насичений кольор.

Основні переваги HSV:

    Інтуїтивне представлення кольору: HSV набагато більше відповідає сприйняттю кольору людиною і робить зміну кольору більш інтуїтивною, особливо при роботі з графічними програмами.

    Легше змінювати відтінок та насиченість: Перетворення відтінку та насиченості у моделі HSV зазвичай є простішими, ніж у RGB, що робить цю модель популярною в дизайні та обробці фотографій.

Хоча HSV не так часто використовується у відображенні графіки, як RGB, вона є корисною для визначення кольорів у більш інтуїтивному та зрозумілому способі.
