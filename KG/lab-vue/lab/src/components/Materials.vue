Афінні перетворення:

Афінні перетворення - це клас перетворень, які зберігають паралельність ліній, а також відстані та кути між точками. Вони включають такі операції, як трансляція (переміщення), поворот, масштабування та зеркальне відображення.

    Трансляція (переміщення): Зміщення об'єкту на площині на певну відстань у напрямку x та y.

    Поворот: Обертання об'єкта навколо заданої точки або вісі.

    Масштабування: Зміна розміру об'єкта шляхом збільшення або зменшення вздовж обох або одного з координатних напрямків.

    Зеркальне відображення: Віддзеркалення об'єкта вздовж визначеного напряму.

Базові перетворення координат та об'єкту:

    Трансляція координат: Переміщення всіх точок об'єкта на певну відстань у напрямку x, y та/або z.

    Поворот координат: Обертання точок об'єкта навколо координатного початку або іншої точки.

    Масштабування координат: Збільшення або зменшення розмірів об'єкта, розташованого вздовж координатних напрямів.

    Матричні перетворення: Використання матриць для представлення афінних перетворень та їх ефективного застосування до координат об'єктів.

Ці математичні концепції і перетворення є основними для реалістичного моделювання та синтезу рухомих зображень в комп'ютерній графіці та відтворенні тривимірних об'єктів на екрані. Вони використовуються у програмах для створення відмінних візуальних ефектів, анімації та ігор.
